void	ft_swap(int *a, int *b)
{
	int	c;

	c = *a;
	*a = *b;
	*b = c;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int	i;

	i = 1;
	while (i <= (size / 2))
	{
		ft_swap(&tab[i - 1], &tab[size - i]);
		i++;
	}
}
