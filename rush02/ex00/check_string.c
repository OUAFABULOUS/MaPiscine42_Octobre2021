long long int	ft_atoi_2(char *str)
{
	int				i;
	long long int	n;

	i = 0;
	n = 0;
	while (((str[i] >= 9 && str[i] <= 13) || str[i] == ' '))
		i++;
	if (str[i] == '-')
		return (-1);
	if (str[i] == '+')
		i++;
	if (str[i] < '0' || str[i] > '9')
		return (-1);
	while ((str[i] >= '0' && str[i] <= '9') && (n <= 4294967295))
	{
		n = (n * 10) + str[i] - '0';
		i++;
	}
	if (n > 4294967295)
		return (-1);
	return (n);
}
